    private void fillTable() {
        List<Product> products = pDao.all();
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        model.setRowCount(0);
        for (Product p : products) {
            model.addRow(new Object[]{p.getId(), p.getName(), p.getDiscription(), p.getLink()});
        }
    }
    
    private void fillTable(String sName) {
        List<Product> products = pDao.all(sName);
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        model.setRowCount(0);
        for (Product p : products) {
            model.addRow(new Object[]{p.getId(), p.getName(), p.getDiscription(), p.getLink()});
        }
    }

    private void setValues(Product p) {
        tfProductName.setText(p.getName());
        taProductDisc.setText(p.getDiscription());
        tfProductLink.setText(p.getLink());
        try {
            if(p.getPhoto1() != null && p.getPhoto1().getPhoto() != null) {
                Image image = ImageIO.read(p.getPhoto1().getPhoto());
            ImageIcon icon = new ImageIcon(image);
            lblFile1.setText("");
            lblFile1.setIcon(new ImageIcon(icon.getImage().getScaledInstance(lblFile1.getWidth(),
                    lblFile1.getHeight(), Image.SCALE_SMOOTH)));
            }
            
            if(p.getPhoto2() != null && p.getPhoto2().getPhoto() != null) {
                Image image2 = ImageIO.read(p.getPhoto2().getPhoto());
            ImageIcon icon2 = new ImageIcon(image2);
            lblFile2.setText("");
            lblFile2.setIcon(new ImageIcon(icon2.getImage().getScaledInstance(lblFile2.getWidth(),
                    lblFile2.getHeight(), Image.SCALE_SMOOTH)));
            }
            
            if(p.getPhoto3() != null && p.getPhoto3().getPhoto() != null) {
                Image image3 = ImageIO.read(p.getPhoto3().getPhoto());
            ImageIcon icon3 = new ImageIcon(image3);
            lblFile3.setText("");
            lblFile3.setIcon(new ImageIcon(icon3.getImage().getScaledInstance(lblFile3.getWidth(),
                    lblFile3.getHeight(), Image.SCALE_SMOOTH)));
            }
            
        } catch (IOException ex) {
            Logger.getLogger(productv.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

