    private void attachFile1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_attachFile1ActionPerformed
        // TODO add your handling code here:
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Image Files",
                "jpg", "png", "gif", "jpeg");
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(filter);
        chooser.showOpenDialog(this);
        File f = chooser.getSelectedFile();

        if (f != null) {
            try {
                photo1 = new FileInputStream(new File(f.getAbsolutePath()));
            } catch (FileNotFoundException ex) {
                Logger.getLogger(productv.class.getName()).log(Level.SEVERE, null, ex);
            }
            ImageIcon icon = new ImageIcon(f.toString());
            lblFile1.setText("");
            lblFile1.setIcon(new ImageIcon(icon.getImage().getScaledInstance(lblFile1.getWidth(),
                    lblFile1.getHeight(), Image.SCALE_SMOOTH)));
        }

    }//GEN-LAST:event_attachFile1ActionPerformed

